int	ft_sqrt(int nb)
{
	int	sqrt;

	sqrt = 1;
	if (nb < 0)
		return (0);
	if (nb < 2)
		return (nb);
	while (sqrt < nb / sqrt)
		sqrt++;
	if (nb == (sqrt * sqrt))
		return (sqrt);
	return (0);
}
