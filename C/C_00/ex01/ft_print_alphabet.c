#include <unistd.h>

void	ft_print_alphabet(void)
{
	char	alpha;
	int		i;

	alpha = 'a';
	i = 0;
	while (i < 26)
	{
		write(1, &alpha, 1);
		i++;
		alpha++;
	}
}
