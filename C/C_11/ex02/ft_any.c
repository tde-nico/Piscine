int	ft_any(char **tab, int (*f)(char*))
{
	int	i;
	int	res;

	res = 0;
	i = 0;
	while (tab[i])
	{
		if ((*f)(tab[i]) != 0)
		{
			res = 1;
			break ;
		}
		i++;
	}
	return (res);
}
