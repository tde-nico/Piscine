int	ft_count_if(char **tab, int length, int (*f)(char*))
{
	int	fi;

	if (length == 0)
		return (0);
	fi = 0;
	if ((*f)(tab[0]) != 0)
		fi = 1;
	return (fi + ft_count_if(tab + 1, length - 1, f));
}
