int	ft_str_is_lowercase(char *str)
{
	int	i;
	int	alpha;

	i = 0;
	while (str[i] != '\0')
	{
		alpha = 1;
		if (('a' <= str[i]) && (str[i] <= 'z'))
			alpha = 0;
		if (alpha)
			return (0);
		i++;
	}
	return (1);
}
